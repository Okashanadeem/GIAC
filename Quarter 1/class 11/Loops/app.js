// For Loops 
// - In For loop first we declare then initialize a variable 
// - Then we give a condition 
// - then we the incriment or decrement 
// - Then we give the work to do in curly brackets 
// [We use For loop when we know the limit]
for (var i = 1; i <= 10; i++) {
    console.log(i);
}
;
// While loops 
// - In While loop we declare and initialize a variable 
// - Then we start the while loop 
// - In round brackets we give the condition 
// - Then we give the work to do 
// - then we give the incriment or decrement 
// [We use While loop when we dont know the limit]
var num1 = 1;
while (num1 <= 10) {
    console.log(num1);
    num1++;
}
;
// Do While Loops 
// - In Do-While loop we declare and initialize a variable 
// - Then we start the while loop 
// - Then we give the work to do in curely bracket
// - then we give the incriment or decrement  
// - then we call the while loop 
// - And then in round brackets we we give the condition 
// [We use Do-While loop when we necessarly want a loop first]
var num2 = 1;
do {
    console.log(num2);
    num2++;
} while (num2 <= 10);
